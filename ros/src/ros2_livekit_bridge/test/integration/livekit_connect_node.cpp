/*
 * Integration test node: LiveKit connect
 *
 * A minimal ROS2 node that mirrors robot_stub.cpp. It connects to a LiveKit
 * room and streams synthetic audio (440 Hz sine tone) and video (dark-blue
 * background with animated frame-counter and elapsed-time overlay) using the
 * livekit_bridge library. Its purpose is to prove that livekit_bridge links
 * and functions correctly inside a ROS2 colcon build.
 *
 * Parameters:
 *   livekit_url   (string) – WebSocket URL, e.g. ws://localhost:7880
 *   livekit_token (string) – JWT participant token
 *
 * Usage (after colcon build with -DBUILD_INTEGRATION_TESTS=ON):
 *   ros2 run ros2_livekit_bridge livekit_connect_node \
 *     --ros-args -p livekit_url:=ws://... -p livekit_token:=<token>
 */

